    @Override
    public synchronized Product getProduct(Long id) {
        try {
            return productList.stream()
                    .filter((p) -> p.getId() == id && p.getCurrency() != null && p.getStock() > 0)
                    .findAny().get();
        }
        catch (Exception e){
            System.out.println("Can't get access");
            return null;
        }
    }
    @Override
    public synchronized List<Product> findProducts(){
        return productList.stream()
                .filter((p)->p.getCurrency()!=null && p.getStock()>0)
                .collect(Collectors
                        .toList());
    }

    @Override
    public synchronized void save(Product product) {
        product.setId(productList.size()>0 ? productList.size() + 1 : 1L);
        productList.add(product);
    }

    @Override
    public synchronized void delete(Long id) {
        try {
            productList.remove(productList.stream()
                    .filter((p) -> p.getId() == id)
                    .findAny()
                    .get());

            productList.stream()
                    .skip(id-1)
                    .forEach((product -> product
                            .setId(product.getId()-1L)));
        }
        catch (Exception e){
            System.out.println("Ð¡annot be deleted");
        }
    }
